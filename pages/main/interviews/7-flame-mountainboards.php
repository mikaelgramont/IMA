  Speaking of dirtpipes. Are the Reactors a street truck and dirt pipes dirt?
</p>
<p>
  No its actually the other way around. There is the aggressive boarding adapter available for the Dirtpipe which turns your normal dirtboard to a streetboard.
</p>

<p class="question">
  You mentioned electric motors?
</p>
<p>
  Yes we have been developing the system for 3 years already. Flame sells the hardware. The electrical pieces are sold by ESK8.de (close partner).
</p>

