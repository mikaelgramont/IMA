<p class="question">
  How many different designs of trucks have you had in the past?
</p>
<p>
  There are two overall types. The Transporter and the Dirtpipe. The improved and new model of the Transporter is the Reactor which can be bought this year for the first time. The Dirtpipe already had two models for sale, versions 1.0 and 2.0 (the improved version). Version 3.0 is in progress.</p>

<p class="question">
  Are Flame trucks torsion trucks, cube trucks?
</p>
<p>
