<p class="question">
  What makes the Reactor trucks different from the Transporter or Dirtpipes?
</p>
<p>
  As already mentioned the Transporter is kind of the older brother of the Reactor.<br>
  So the Reactor is the improved Rransporter. Also the Reactor is something for fans who like the design. Its kind of the luxury Flame truck which you buy for the looks. Or as my dad says it: it's for the smile.<br>
  The practical model is the Dirtpipe to which all the add ons are sold. Like the electric motors, the aggressive boarding adapter, or the hydraulic braking system.
</p>

