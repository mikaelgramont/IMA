<h1 class="display-font">Getting to know... Ola Tomalczyk</h1>

<p>In anticipation of the 2018 World Championships approaching, we decided to interview Ola Tomalczyk. Your 2017 Women’s World Champion and fairly new blood to the sport. Wondering how she did it? Well, we have her story here.</p>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/ola-1.png" alt="Portrait of Ola Tomalczyk">
</p>

<p>
    First off Ola is 20 years old and from the town of Żywiec in Poland. She has been riding for 3 and a half years and progressed quickly enough to be a top competitor on the world stage. Along with her current World Championship title she has also been the Polish Women’s Champion from 2015-2017. As well as podium positions at Dirt Dessert (the location for this year’s World Championship) in 2015 and 2017.
</p>

<p>
    Ola started as a skateboarder. But growing up surrounded by mountains she looked for a crossover sport to enjoy it all. While searching the internet she discovered mountainboarding. While still snowboarding, dirt biking, longboarding, climbing and working out she never found the same feeling that mountainboarding gave her.
</p>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/ola-2.png" alt="Ola on a snowboard">
</p>

<p>
    All of this was fueled by the mountainboard community in Poland. A strong scene that feels like an extended family. Ola says there is always someone to ride with and they are constantly progressing the sport. When they go riding they train hard. Every session is fun. Discovering new spots and spending time growing the local community is key. Pushing each other and thinking of creative ways to express themselves on boards.
</p>

<p>
    She loves how universal a mountainboard can be. From sliding rails in her backyard, to riding a boardercross track in Rybnik, or hitting Dawid’s jumps at the Mosquito Spot. She can do it all, and well. With her loving all styles of mountainboarding it is no wonder she has become such a well rounded athlete. When asked what her favorite trick is she said she likes to keep it stylish with a nose grab while kicking her back leg out and pulling the deck in close.
</p>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/ola-3.png" alt="Ola goes mountainboarding in the forest">
</p>

<p>
    We asked her why there were so few female mountainboarders and what changes she thought could help? To which she responded:
</p>

<p class="quote">
    “I ask myself this question everyday. Women have different needs, most of them don’t need adrenaline in their life. But there are other problems. Many have never heard about mountainboarding and many don’t know how to start.
</p>
<p class="quote">
    I try everyday to change it. This year I became an ambassador for the page “Damska Deska” (it means “girl’s board”) which have one purpose: to disseminate action sports in a circle of Polish women by articles and events. I talk with girls on ski slopes, school, shops… everywhere where I see a chance, and i try to show them how amazing snowboarding and mountainboarding can be. So I think the most important is to show women that action sports exist and are for us!”
</p>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/ola-4.png" alt="Ola riding at the Dirt Dessert event">
</p>
<p>
    Look for her in the future at the Dirt Dessert World Champs, your local competition, or your local park. She’s a well rounded athlete with a good spirit and we look forward to watching her grow in the future!
</p>

