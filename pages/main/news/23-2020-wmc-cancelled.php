  <h1 class="display-font">The 2020 World Mountainboard Championship is cancelled</h1>
  <p>
    With recent circumstances in mind the Association Mountainboard Compiegne in conjunction with the International
    Mountainboard Association have cancelled the 2020 World Mountainboard Championships. It is with a heavy heart that
    we make this decision.<br>
    The current crisis has made it hard to finish the work and get logistics in place in time to
    hold a world class event. With disruptions in the local French healthcare system, injuries at the event could pose a
    problem and be very dangerous. Athlete’s travel have also been affected at the writing of this. Taking these factors
    amongst others in mind a joint decision has been made to cancel.
  </p>

  <p>
    As the COVID-19 crisis began to unfold, the discussion between the AMC and the IMA started as a postponement. After
    monitoring the pandemic, watching other industries actions, and weighing all of the options for over a month the
    final move to cancel was made.
  </p>
  <p>
    Here are the circumstances that led us to this conclusion:
  </p>
  <ul>
    <li>
      Mountainboarding, like other sports, comes with an inherent risk of injury. In the context of a competition the
      size of the WMC, we need to be assured the healthcare system is strong. Right now we cannot assume the local
      healthcare system in Compiegne will be safe enough or have enough resources to treat multiple, possibly traumatic,
      injuries.
    </li>
    <li>
      All travel to and from France is frozen at this moment. That may not be the case in July, but we cannot expect
      international athletes to invest in costly travel without a definitive outcome. We are aware of flights that have
      already been cancelled for the event.
    </li>
    <li>
      Many athletes have been laid off or furloughed. Small businesses will suffer this year as the world economy
      struggles. Asking athletes to travel and invest in the WMC may put financial stress on them or their families. We
      cannot ask this of our community.
    </li>
    <li>
      The lower numbers at this year’s WMC will have also financially strained its organizers and staff. With money
      invested in the competition, running the WMC just to have a competition wouldn’t be cost effective. The AMC will
      run a top international event in the future, the dates and format of which are still to be determined.
    </li>
  </ul>

  <h2 class="display-font">The Before Camp and the 4 Down Project</h2>

  <p>
    These events are continuing as planned!
  </p>

  <p>
    The Before Camp is Friday July 10th to Monday July 13th, 2020. Being a smaller regional event, it will go on as
    scheduled.<br>
    <b>On top of that, the AMC will still host a more informal summer camp</b> at the same time (July 15th-18th) and
    same place as the WMC, but it won't be the World Championship anymore.<br>
    This means that for those still willing and able to take a road trip, you can have a week of fun at the French parks.
  </p>

  <p>
    The 4 Down Project is on!
    The filming period will extend to 5 months to deal with the social distancing, isolation, and quarantines currently
    in place. Projects will premier in an online format on the 1st of September, 2020.
  </p>

  <h2 class="display-font">In conclusion</h2>

  <p>
    As we all know by now, 2020 is going to be a very strange year, and we're all adjusting to it. We're obviously sad
    that we had to make this decision, and we will miss seeing everyone this summer, but rest assured we will make it up
    to you next time!
  </p>

  <p>The AMC and the IMA (Ludo, Kody, Matt and Mika)</p>

