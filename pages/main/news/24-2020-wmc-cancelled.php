    </li>
    <li>
      All travels to and from France are frozen right now. That probably won't be the case come July, but we cannot
      expect all international athletes to buy very expensive last-minute plane tickets to fly to France for the event.
    </li>
    <li>
      A lot of small businesses will suffer this year, as the world economy takes a big hit. Because of that, we know we
