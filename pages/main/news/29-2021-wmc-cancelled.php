  <h1 class="display-font">The 2021 World Mountainboard Championship is cancelled</h1>
  <p>
    It is with heavy hearts that the International Mountainboard Association in conjunction with the Association
    Mountainboard Compiegne have made the decision to postpone the 2021 Championship.
  </p>
  <p>
    There were many talks with each other regarding this decision and it was a tough call to make.
  </p>
  <p>
    Many of the concerns raised last year are the same reasons we have come to this conclusion. Such as travel
    restrictions, lockdowns, financial burdens on athletes, and in turn a nominal turnout for a competition known to
    draw the highest number of competitors.
  </p>

  <h1 class="display-font">The Good News: there will still be riding</h1>
  <p>
    Look for the teams in France to put on gatherings. It’s not that we didn’t think anything should happen on these
    dates, only that the World Championships was not the best choice. The good news is that to make up for this, the
    teams at AMC and at LA Mountainboard are preparing a low key, week-long mountainboard event at their respective
    parks.
  </p>
  <p>
    So for those who were looking forward to riding the epic Compiegne park, you can still do so, and also check out the
    amazing LA Mountainboard park in Rouans (near Nantes)! Full details will be released soon but the rough idea is the
    following :
  </p>
  <ul>
    <li>
      July 10th-13th: <a href="https://www.youtube.com/watch?v=LjqbyX2QrNg">LA Mountainboard park</a>,
      <a href="https://www.youtube.com/watch?v=6keieQRNh7k">Nantes skatepark</a>,
      <a href="https://www.youtube.com/watch?v=h_56YxcTc14">Abbaretz open mine</a>
    </li>
    <li>
      July 14th: on the road to Compiegne
    </li>
    <li>
      July 15th-18th: riding in Compiegne (their park, nearby forest tracks, and other things)
    </li>
  </ul>
  <p>
    If you've been to either or both of these parks, you'll be happy to hear that they have both undergone large amounts
    of work, and there will be new things to ride!
  </p>

  <h1 class="display-font">More Good News: the 4 Down Project is still on!</h1>
  <p>
    That's right! Another 4 teams have been selected to participate in this year's annual video contest.
