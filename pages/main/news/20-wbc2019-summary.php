<h2 class="display-font">Freestyle in Poland, Boardercross in Serbia!</h2>
<p>
  As you may know, this year's World Mountainboard Championship wass split into two events, with Freestyle taking
  place in Poland in July (see <a href="<?php echo BASE_URL?>news/14-wfc2019-summary">our WFC19 report here</a>), and
  Boardercross in Serbia.
</p>
<p>
  As per usual, athletes from many countries came to measure themselves against the best mountainboarders. A few
  veteran athletes from the USA made the trip, including Akoni Kama and Josh Knepper, who both were there from the very
  early days of US mountainboarding. Seeing them back on boards was a treat. Akoni even pledged to throw a backflip
  on the freestyle course after almost a decade off!
</p>

<h2 class="display-font">The track</h2>

<p>
  Serbia's held the World Championships several times now, last time being in 2016. Everyone who's been to the track
  there knows what a special place it is, minutes from the city, and yet sheltered from the noise and the craze.
</p>

<figure class="photo-wrapper">
  <img class="photo"
       src="../images/news/20-wbc2019-summary/gates.jpg" alt="A photo of the start gates">
  <figcaption class="photo-caption">
    The start gates
  </figcaption>
</figure>

<p>
  Since 2016, the course was modified in specific spots in order to let athletes manage speed better, which was always
  a challenge. The biggest change though, was the addition of a new feature in mountainboarding: a corkscrew bridge!
  The feature proved challenging for regular-footers as the following berm made it difficult to navigate. In the end,
  that did not end up favoring goofy-footers too much as the Pro finals had 2 athletes of each stance.
</p>

