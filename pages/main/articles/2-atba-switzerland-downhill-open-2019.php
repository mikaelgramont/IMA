<p>
  At the beginning of the race, riders started with some easy runs. Riding a 3.8 km long downhill trail is quite
  exhausting. Some riders even took a rest half way. But starting to compare their times at the bottom, the time attack
  has started. Riders pushed more and more, working out strategies to have a clear run, and improved their times over
  and over. Marco Dähler did some of the fastest times of the day and took the 1st place. TJ got very close in one of
  his runs, but could not improve with his other attempts and finished 2nd. The biggest fight happened for 3rd Place.
  For a long time David Hutter was leading, having Luzi Kölla close in his neck. But it was Kalle Janousch who did huge
  improvements in each run and jumped ahead of David and Luzi in his last run of the day.
</p>

