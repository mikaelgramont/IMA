<p>
  The racing mode was completely open. All riders in one category. Every rider had the chance to do as many runs as he
  wanted, within a time window of 5 hours. Even for time measuring, everyone was responsible himself. We were using GPS
  based time measuring, supported by the <a href="https://racechrono.com/">RaceChronoApp</a> on the riders smartphones.
</p>

