<h1 class="display-font">By Marco Dähler</h1>
<p><time>September 17th, 2019</time></p>

<p>
  The ATBAswitzerland has held the Swiss Downhill Open on August 17 in Flumserberg. It has been a while since
  Switzerland has seen a Mountainboard competition for the last time. It was 6 years ago in Appenzell and 9 years ago
  in Fruttigen, to be exactly. This year 2019 is very special for the ATBAswitzerland, we celebrate our 10th
  anniversary. So it was really important to us to celebrate this with a Mountainboard Downhill Open.
</p>
<p>
  Switzerland has no Mountainboard centers, but we have many Bike parks which allow mountainboarders. One of the best
  bike downhill trails is the Blue Salamander in Flumserberg, because it is nice and flowy over 3.8 km, with loads of
  banks, rollers, jumps and wallrides and most important, you can ride it with and without brakes.
</p>

