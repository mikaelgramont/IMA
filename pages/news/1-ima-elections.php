<h1 class="display-font">2018 Elections</h1>

<h2 class="display-font">What’s happening - TLDR;</h2>
<p>The IMA is setting out to renew the board and elect a new president.</p>

<h2 class="display-font">The context</h2>
<p>The IMA was founded by Diego Anderson (FR) and led by him for many years after that.<br>
In early 2016, an election was held through the <a href="https://www.facebook.com/groups/mountainboardworld/">Facebook group</a>, and it resulted in the election of Dave Stiefvater as new president of the IMA.</p>
<p>Since the election, it has been difficult for the president and the appointed board members to build momentum to get things done and push mountainboarding forward. Many people have become frustrated, and it is the IMA’s hope that a new, more formal election process will result in a better organization with clearer goals, roles and responsibilities.</p>

<h2 class="display-font">How it works</h2>
<p>We would like to call for candidates to the role of president to step up by <a href="https://docs.google.com/forms/d/18xwF06Ziit7y3PnvuLpftZLQFIEO5sKn13TDGatI3Vg/edit">filling this form</a> <b>before December 15th</b>.<br>
As a candidate, please highlight your experience with mountainboarding and/or other sports and activities, as well as the actions you would take as president (ie, why people should vote for you), and maybe the team you would bring in with you.</p>
<p>After the application deadline (December 15th), we’ll post the list of candidates here on the IMA site, and we’ll then proceed with an online vote taking place over a period of two weeks: from <b>Jan 1st to Jan 15th 2018</b>.</p>
<p>At the end of the vote, the new president will take over and proceed with their plan.</p>
