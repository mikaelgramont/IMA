 or just to be right but it will be a good way to settle shit it a good way. i'm not saying that I as the new President plan to disrupt the acting structure which makes the IMA work as well as it does today just to disrupt it but rather to add a function which I'm willing to facilitate as a mediator and or arbitrator. </p>
