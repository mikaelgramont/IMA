<h1 class="display-font">IMA presidential candidates for 2018</h1>

<p>Here is the list of people running for the 2018 election, along with their plan, and their proposed board members.</p>

<p>As you probably know by now, the IMA is setting out to renew the board and elect a new president.</p>

<p>On Dec 15th, we reached the deadline for new applications, and so we now have the list of running candidates. The actual vote will take place over the first two weeks of 2018, so by January 15th, we will have a new President.</p>

<p>Click on each of the candidates, see what they had to say, and maybe download their program. Once you've made up your mind, we'll see you here again in 2018 for the vote!</p>

