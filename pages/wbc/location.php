      <h2 class="display-font">The race course</h2>
      <p>
        The track is located in Bukovac, 15 minutes from the athlete hostel
      </p>
      <p>
        Arguably the fastest track in the World. Just over 700 meters long athletes clear the course in 52 seconds.
        Athletes must navigate 14 turns, numerous rollers and jumps to make it to the finish corral. A challenge for
        even the world’s best but also fun if you manage to slow it down. With the new left hand corkscrew this track
        will possess new challenges and level the regular vs goofy footed playing field.
      </p>
      <p>
        <b>ATB Park Bukovac</b><br>
        Partizanska,<br>
        Bukovac 21209,<br>
        Serbia<br>
