      <a id="nightlife"></a>
      <h2 class="display-font">Nightlife</h2>
      <p>
        Again there are many places to enjoy the nightlife in Novi Sad. Most groups will go out together but you will
        never fail to see your fellows athletes on Laze Teleckog. Here are a few of our favorite locations:
      </p>
      <ul class="details-list">
        <li>
          <p>
            <b>Puberaj</b><br>
            The location of registration and the “4 Down  Project”. This bar has a great atmosphere and is a local
            hangout spot. Head here for a couple of drinks and a little quieter experience.<br>
            <a href="https://goo.gl/maps/hQfqXReA5cJQoJX79">[Google Maps]</a>
          </p>
        </li>
        <li>
          <p>
            <b>Shamrock Bar</b><br>
            The local Irish bar in Novi Sad. A locals favorite with a fun cocktail list. You can expect to enjoy a fun
            night here on Wednesdays.<br>
            <a href="https://goo.gl/maps/K1WdQGKcbvtW2EMB9">[Google Maps]</a>
          </p>
        </li>
        <li>
          <p>
            <b>Rakija Spirit Bar</b><br>
            Well versed in Rakija (the local serbian drink) this bar has them all. Pop in to try a couple and then head
            on to enjoy Laze Teleckog. Warning: Don’t stay here to long, things can get blurry.<br>
            <a href="https://goo.gl/maps/MwkdhmkJ3ajJwULb7">[Google Maps]</a>
          </p>
        </li>
        <li>
          <p>
            <b>Lazino Tele</b><br>
            Out of all the bars on Laze Teleckog this one is special. 2 floors of fun and enough room to accommodate all
            of the athletes it always seems to be where everyone ends up at the end of the night. If you come here
            beware, you may have the time of your life and win a world championship.<br>
            <a href="https://goo.gl/maps/WkzgXaEHpC7zwvaE6">[Google Maps]</a>
          </p>
        </li>
        <li>
          <p>
            <b>Cuba Libre</b><br>
            This location is small, sweaty, and the bar you want to be at if you want to have to much of a good time.
            When you walk in keep an eye out for Jereme Leafe. If you find him he’ll buy you a bottle of rakija and
            you’ll watch the sunrise. A staple of the Serbian World Championships.<br>
            <a href="https://goo.gl/maps/JCWTJ3ycgoY5vrgu9">[Google Maps]</a>
          </p>
        </li>
      </ul>
