<h1 class="display-font">Getting to know... Yuya Nakayama</h1>

<p>We would like to introduce you to Yuya Nakayama and the Japanese scene. Yuya is 35 years old and has been riding for 21 years. Yuya is from <a href="https://goo.gl/maps/jak4nPWVWSk">Fujinomiya</a> and rides at the YMCA center near Mt Fuji. </p>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/yuya-1.png" alt="Portrait of Yuya Nakayama">
</p>

<p>
    When asked what his favorite discipline within mountainboarding is, he said all of them. He has been leading the mountainboard scene in Japan for years and his favorite trick is a backflip.
</p>

<p>
    Yuya goes on solo missions into the forest and finds lines in the trees. He has competed internationally attending the 1998 World Champs and the 2006 Fat Face Freestyle World Championships. Yuya also enjoys snowboarding, skating, and rafting in his free time. He’d love to see a dirt halfpipe built and loves his native scene.
</p>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/yuya-2.png" alt="Yuya Nakayama has a sick indy">
</p>

<p>
    He shed some light on a scene that most people know nothing about. Japan has a lot to offer and the Japanese athletes take advantage of it all. From carving up skateparks to getting technical in the forest they really embrace the spirit of mountainboarding.
</p>

<p>
    Yuya helped explain some of what is going on with the scene in Japan. “We bring kickers to the local park and find lines in the forest to ride. Japan is special because everything is compact. Access from spot to spot is quick and easy”. They have resorts to ride with “golf course” grass and rope tow so it’s great for carving. They have also made a dirt park with jumps, rails, and a track!
</p>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/yuya-3.png" alt="Yuya Nakayama has a sick backside air">
</p>

<p>
    Yuya says his favorite part of mountainboarding is spending time with other athletes. Whether at a competition or local riding spots. With all his enthusiasm he wants us all to “Find lines and keep it 4 down bro!!”
</p>


