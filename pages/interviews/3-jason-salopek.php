<h1 class="display-font">Getting to know... Jason Salopek</h1>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/jason-1.png" alt="Portrait of Jason Salopek">
</p>

<p>Jason Salopek. Have you ever heard the name? There’s a chance you haven’t, but if you have, you know what he is capable of. This 24 year old from Melbourne, Australia burst on the scene three and a half years ago. Dylan Warren has taken him under his wing and is teaching him talents some of us can only dream of. Jason has never participated in a competition but his riding is progressing quickly, favoring tricks such as front 3’s and back 50’s on rails.</p>


<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/jason-2.png" alt="Jason Salopek doing a 5-0">
</p>

<p>
    Jason hails from Australia. Many have never been there to ride but he gave us insight into what it’s like. They don’t have a designated mountainboard center (yet) so they meet at indoor skateparks, on the street, or at local dirt riding spots. This gives them access to train at a park with resi ramps, transitions, or rails and translate that into their riding styles. This has helped produce the largest street and park scene in the world with style becoming a necessity over chasing the biggest tricks. It makes the Australians a very well rounded group of athletes that can literally ride anywhere.
</p>


<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/jason-3.png" alt="Jason Salopek dropping from a roof">
</p>

<p>
    Back to Jason. This means he rides with mountainboard legends such as Dylan Warren and Joel Lee! He is also one of the few athletes that has traveled to Japan and reports that it is amazing. With upbeat sessions and a progressive atmosphere he is being guided to become one of the best up and coming mountainboard athletes.
</p>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/jason-4.png" alt="Jason Salopek boardsliding a handrail">
</p>

<p>
    On a more personal note he is a graphic designer who also enjoys skating, snowboarding, golfing, and playing Australian rules football. When asked what mountainboarding means to him he quoted:
</p>
<p class="quote">
    “Having a good time with your mates and to shred till ya dead! I want to see more late back 1’s and less double backies. Less focus on competition and more focus on making mountainboarding fresh and relatable with regular new content and plenty of community involvement! Style matters!”
</p>

