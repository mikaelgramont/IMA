<h1 class="display-font">Getting to know... Haero Boards</h1>

<p>
  Mountainboarding has always been an evolving sport. It started with steel framed boards, boards with 20” rims, in-line boards, or even boards with 8 wheels! Once uni-body decks came out in 1999-2000 the progression of decks seemed to slow. Companies dialed in flex patterns and deck lengths. While trucks, wheels, and bindings took the forefront of progression. But in the last few years decks have seen an influx of technology. Concave re-entered the scene across the board and revolutionized the standard in mountainboarding. Rider owned companies popped up all over the world with each one making their dreams of a perfect mountainboard a reality.
</p>

<p>
  One of the most anticipated iterations of these is from the German crew at Hero Boarding. They built what is arguably one of the most progressive mountainboards to date. Taking all of the best ideas over the years and combining them into one. Does concave, a p-tex base, and stiff construction pique your interest? It did ours, so we sat down with the mastermind of this project to see what the future holds for them.
</p>

<p class="question">
  Mr Phil Heinle please introduce yourself.
</p>
<p>
  Hey guys, im Phil from Germany and I am a mountainboard addict since 2009.
  I was always into shaping my dreams and bring them to life. Starting with shaping my own dirts to building a World Championship track ending up with shaping the mountainboard deck we always dreamed of.
</p>

<p class="interview-image">
  <img src="<?php echo BASE_URL ?>images/interviews/6-haero/grosserlach.jpg" alt="Photo of the Grosserlach mountainboard center in Germany">
</p>

<p>Dreams can come true if you stick to them!</p>

<p class="question">
  How long have you been running “Hero Boarding”?
</p>
<p>
  I think it was around 2012 when I took over the small Mountainboard Shop Hero Boarding wich started 1997 as a Kite Shop and got into Mountainboarding 2000-ish. First I was a team rider and supporter for them. As the old owner decided to stop running the shop because it wasn’t worth to run it further, I did not want this brand to die and left the market to ebay or non riders owned shops. So I offered to take over his business for saving the german community to be left with crappy boards and service. Surely it was nothing to get a millionaire :D
</p>

<p class="interview-image">
  <img src="<?php echo BASE_URL ?>images/interviews/6-haero/hero-teamrider.jpg" alt="Old photo of hero team rider Phil Heinle">
</p>

<p class="question">
  So we’ve seen Joel Betzler riding a deck called the Bro-Model for a couple years now. It is definitely one of a kind. Tell us about how that came up and what makes it different from other decks on the market?
</p>
<p>
  Yeah its almost 2 Years now Joel shredding our DIY Deck. Basically it was Joel forcing me to go that step.  I recognized his talent really fast and took him under contract as a team rider.
</p>
<p class="interview-image">
  <img src="<?php echo BASE_URL ?>images/interviews/6-haero/joel-start.jpg" alt="Joel Beltzer board sliding a rail">
</p>
<p>
  After he was riding an old Kite-Landboard, he asked me to hook him up with the new concaved Decks which been the “latest thing” at that time. Unfortunately they´ve not been strong enough to handle his almost daily shredding in the Park. It ended up with monthly broken Decks. We always got replacement but he couldn't ride in the waiting time. I told him, wait for Colab to hit the market and ride the strong cap composite decks in the waiting time. But he was really impatient and was always asking, when are those new decks hitting the market? Kody, I think you remember me asking you remotely for the releasing date?! haha.
</p>
<p>
  As I have some friends working in the snowboard and longboard industry, we've been confident enough to work on our very own vision of the best mountainboard deck together with their help. Funny Fact is, it was me chatting with TRAMPA to build a concaved deck back in Summer ´14. Unfortunately this never happened.
</p>
<p>
  So what makes it different from other decks on the market? Hmm... In short, our Deck will be the same as others when it comes to Deck length and Truck angle but everything else is kind of different with some minor and some bigger changes. Is that enough?
</p>

<p class="interview-image">
  <img src="<?php echo BASE_URL ?>images/interviews/6-haero/deck1.jpg" alt="The BroModel deck">
</p>

<p class="question">
  I think that doesn’t satisfy my and our followers expectations. Rumors say, that you are going to start producing decks for sale to the public. Please spill some of the secrets and tell us the features that will put this deck on the map as one of the best in the world.
</p>
<p>
  Haha okay I knew you wanna know more details. The Rumors are true. Let's get into detail about the why first.<br>
  As we built that first prototypes for us, it was never our intention to go public with them. But as <a href="https://www.instagram.com/p/BXvXvAEBPpO/?utm_source=ig_web_copy_link">we tested the first working prototypes with other riders</a>, we got really mind blowing feedback and many of them asking for the same deck.
</p>
<p>
  This was the time we decided to go one step further. We tried to convince our good old friend JoMo from Flame Mountainboards to build a new mould and build some Decks for us and friends. Unfortunately we found out a mould which could be strong enough to give birth to a small batch was too expensive for some decks for friends. The decks would´ve been more than 400€ in retail Price if you recalculate the set up costs for 10 Decks. So JoMo couldn’t help us without charging us a big amount of cash. We thought this project will never come alive beside our 2 running prototypes :D but people kept asking for when we want to build that decks for them haha so we put our minds back together and came up with the idea of making this a community project. We found another producer which had cheaper material prices and calculated, we could afford the set up costs if we could get 30 Decks sold to the public.
</p>

<p class="interview-image">
  <img src="<?php echo BASE_URL ?>images/interviews/6-haero/deck2.jpg" alt="The BroModel deck">
</p>

<p>
  So yeah that’s what we want to do now! Asking 30 of our mountainboard friends to buy one of our shred tools, to make this innovation happen. With selling 30 Decks we can afford to produce a batch of 50 Decks. Enough to hook up our new team riders with Decks and to give away some of them at the upcoming competitions. We want to achieve this Goal via Fundraising. The Riders can decide if they want this change in the Industry or not.
  Sounds like a plan? Go ahead and support our vision! Follow our Pages <a href="https://www.facebook.com/Haeroboards">on Facebook</a> and <a href="https://www.instagram.com/haeroboards">on Instagram</a> and stay up to date, when we publish our fundraising.
</p>

