<h1 class="display-font">Getting to know... Thiago Solon</h1>

<p>Thiago Solon is 25, lives in Brazil and has been mountainboarding for 21 years. He is a 5-time Brazilian Champion and his favorite trick is a backside corked 540 or 720. More about him.</p>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/thiago-1.png" alt="Portrait of Thiago Solon">
</p>

<p>Thiago’s story is much deeper than his alone. As you can see, how can an athlete that is 25 years old have been riding a mountainboard for 21 years? Well his story starts with his dad, Paulo Solon. How he brought mountainboarding to Brazil and how they created everything they needed to grow a scene in South America.</p>

<p>Paulo was living in Rio de Janeiro and surfing was his passion. In 1996, at the age of 20, he moved to Visconde de Mauá. A place surrounded by mountains and away from the sea. It was then that he saw a small mountainboarding advertisement in a surf magazine saying he could “surf the mountains”. This was his answer. Since boards were scarce he began making his own. He made boards for his friends and built tracks to practice on. The park became known as “Local Trip” and the boards are Local Trip Mountainboards.</p>

<p>This is where Thiago steps in. Since riding boards since he was 4 and having a world class facility to train at, he was only destined to become great. He entered his first contest when he was 12. Not knowing where he would end up he joined both junior and pro classes, eventually winning both. From there he has competed many times all over Brazil, becoming Brazilian Champion and eventually South American Champion.</p>

<p>His favorite discipline is freestyle. Spawning from his background in skating the weightlessness, spinning, and pulling his feet out is what draws him to the sport. Though he loves racing and going fast, it's the freedom to do tricks and ride over all different terrains that keeps his passion alive.</p>

<p class="interview-image">
    <img src="<?php echo BASE_URL ?>images/interviews/thiago-2.png" alt="Thiago Solon does a sick one-footer">
</p>

<p>So far Local Trip has made 800 mountainboards and Thiago has helped in research and development. Making boards stronger and lighter bringing their weight down from 15kg to 9kg, with the help of 6” wheels and skate style trucks. The center itself offers free lessons to locals, rentals, and one of the best, world-class parks to ride a board on. </p>

<p>In his off time he also enjoys skateboarding, soccer, and surfing.</p>

<p>In the future he would like to see greater sponsorship packages so that athletes can live off the sport. Bigger contests and a chance to travel more to ride. Thiago says mountainboarding to him is a lifestyle. It goes beyond the sport itself to give you a direct contact with nature and and a sense of freedom. </p>

